package effective.ch2.i2;

/**
 * NutritionFacts implementing the Java Bean pattern
 */
public class NutritionFactsBean {
    private int servingSize = -1; // Required; no default value
    private int servings = -1; // Required; no default value
    private int calories = 0;
    private int fat = 0;
    private int sodium = 0;
    private int carbohydrate = 0;

    public NutritionFactsBean() {
    }

    public void setServingSize(int val) {
        servingSize = val;
    }

    public void setServings(int val) {
        servings = val;
    }

    public void setCalories(int val) {
        calories = val;
    }

    public void setFat(int val) {
        fat = val;
    }

    public void setSodium(int val) {
        sodium = val;
    }

    public void setCarbohydrate(int val) {
        carbohydrate = val;
    }

    @Override
    public String toString() {
        return "{servingSize=" + servingSize + ", servings=" + servings + ", calories=" + calories + ", fat=" + fat
                + ", sodium=" + sodium + ", carbohydrate=" + carbohydrate + "}";
    }
}