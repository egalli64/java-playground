package effective.ch2.i2;

/**
 * NutritionFacts implementing the Builder pattern
 */
public class NutritionFacts {
    private final int servingSize;
    private final int servings;
    private final int calories;
    private final int fat;
    private final int sodium;
    private final int carbohydrate;

    private NutritionFacts(Builder builder) {
        servingSize = builder.servingSize;
        servings = builder.servings;
        calories = builder.calories;
        fat = builder.fat;
        sodium = builder.sodium;
        carbohydrate = builder.carbohydrate;
    }

    /**
     * The NutritionFacts Builder
     */
    public static class Builder {
        private final int servingSize;
        private final int servings;
        private int calories = 0;
        private int fat = 0;
        private int sodium = 0;
        private int carbohydrate = 0;

        public Builder(int servingSize, int servings) {
            // mandatory parameters
            this.servingSize = servingSize;
            this.servings = servings;

            // optional parameters - initialized to default values
            this.calories = 0;
            this.fat = 0;
            this.sodium = 0;
            this.carbohydrate = 0;
        }

        public Builder calories(int val) {
            calories = val;
            return this;
        }

        public Builder fat(int val) {
            fat = val;
            return this;
        }

        public Builder sodium(int val) {
            sodium = val;
            return this;
        }

        public Builder carbohydrate(int val) {
            carbohydrate = val;
            return this;
        }

        public NutritionFacts build() {
            return new NutritionFacts(this);
        }
    }

    @Override
    public String toString() {
        return "{servingSize=" + servingSize + ", servings=" + servings + ", calories=" + calories + ", fat=" + fat
                + ", sodium=" + sodium + ", carbohydrate=" + carbohydrate + "}";
    }
}
